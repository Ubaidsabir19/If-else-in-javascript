"use strict";
// IF ELSE IF Code Example 
let i = 10;
let j = 5;
if (i > 5) {
    if (j > 2) {
        console.log("i is greater than 5 and j is greater than 2");
    }
    else {
        console.log("i is greater than 5 but j is less than or equal to 2");
    }
}
else {
    console.log("i is less than or equal to 5");
}
