"use strict";
// Nested IF Code Example
let z = 10;
let y = 5;
if (z > 5) {
    if (y > 2) {
        console.log("z is greater than 5 and y is greater than 2");
    }
    else {
        console.log("z is greater than 5 but y is less than or equal to 2");
    }
}
else {
    console.log("z is less than or equal to 5");
}
